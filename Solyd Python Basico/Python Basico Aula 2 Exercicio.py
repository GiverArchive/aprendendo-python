# Formulario Basico Python3
'''Fassa um formulário perguntando Nome, CPF, Idade, Altura, Endereço, Contato'''
print ('Preencha o formulário a seguir\n\n\n')
nome = input('Qual seu nome?\nR: ')
cpf = input('Qual o seu CPF?\nR: ')
idade = input('Qual a sua idade?\nR: ')
altura = input('Qual a sua altura?\nR: ')
endereco = input('Qual o seu endereço?\nR: ')
contato = input('Qual o seu número?\nR: ')

print("Printagem:")
print('Nome:', nome)
print('CPF:', cpf)
print('Idade:', idade, 'anos')
print('Altura:', altura, 'metros')
print('Endereço:', endereco)
print('Número:', contato)